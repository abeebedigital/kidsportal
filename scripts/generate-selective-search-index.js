#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const ROOT = process.cwd();
const OUTPUT_PATH = path.join(ROOT, "selective-search-index.js");

function cleanText(value) {
  return String(value || "")
    .replace(/<[^>]+>/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function titleCaseWord(word) {
  if (!word) return "";
  if (word === "oc") return "OC";
  if (word === "naplan") return "NAPLAN";
  if (word === "nsw") return "NSW";
  if (/^\d+$/.test(word)) return word;
  return word.charAt(0).toUpperCase() + word.slice(1);
}

function fallbackTitleFromFile(fileName) {
  const key = fileName.replace(/\.html$/i, "");
  if (key === "selective-school-exam") return "Selective School Exam Hub";
  if (key === "selective-exam-introduction") return "Selective Exam Introduction";
  if (key === "selective-exam-syllabus") return "Selective Exam Syllabus";
  return key
    .split("-")
    .map(titleCaseWord)
    .join(" ");
}

function normalizeTitle(rawTitle, fileName) {
  const title = cleanText(rawTitle)
    .replace(/\s*[—-]\s*FiveToFifteen(?:\s*Kids Learning App)?\s*$/i, "")
    .replace(/\s*\|\s*FiveToFifteen(?:\s*Kids Learning App)?\s*$/i, "")
    .trim();

  return title || fallbackTitleFromFile(fileName);
}

function extractTitle(html, fileName) {
  const key = fileName.replace(/\.html$/i, "");
  if (key === "selective-school-exam") return "Selective School Exam Hub";
  if (key === "selective-exam-introduction") return "Selective Exam Introduction";
  if (key === "selective-exam-syllabus") return "Selective Exam Syllabus";
  const match = html.match(/<title>([\s\S]*?)<\/title>/i);
  return normalizeTitle(match ? match[1] : "", fileName);
}

function getGroupFromHref(href) {
  if (href.indexOf("selective-geometry-") === 0) return "Selective Geometry";
  if (href.indexOf("selective-maths-") === 0) return "Selective Maths";
  if (href.indexOf("selective-reading-") === 0) return "Selective Reading";
  if (href.indexOf("selective-writing-") === 0) return "Selective Writing";
  if (href.indexOf("selective-thinking-") === 0) return "Selective Thinking";
  return "Selective Exam";
}

function buildSearchableText(item) {
  return [
    item.label,
    item.group,
    item.href.replace(".html", "").replace(/-/g, " "),
  ]
    .join(" ")
    .toLowerCase();
}

function listSelectivePages() {
  return fs
    .readdirSync(ROOT)
    .filter((name) => /^selective-.*\.html$/i.test(name))
    .sort((a, b) => a.localeCompare(b));
}

function buildIndexItems() {
  const files = listSelectivePages();
  return files
    .map((fileName) => {
      const filePath = path.join(ROOT, fileName);
      const html = fs.readFileSync(filePath, "utf8");
      const label = extractTitle(html, fileName);
      const group = getGroupFromHref(fileName);
      const item = {
        href: fileName,
        label,
        group,
      };
      return {
        ...item,
        searchableText: buildSearchableText(item),
      };
    })
    .sort((a, b) => {
      const groupSort = a.group.localeCompare(b.group);
      if (groupSort !== 0) return groupSort;
      return a.label.localeCompare(b.label);
    });
}

function writeIndexFile(items) {
  const header = [
    "/*",
    " * Auto-generated by scripts/generate-selective-search-index.js",
    " * Do not edit manually.",
    " */",
  ].join("\n");
  const body = "window.SELECTIVE_SEARCH_LINKS = " + JSON.stringify(items, null, 2) + ";\n";
  fs.writeFileSync(OUTPUT_PATH, header + "\n" + body, "utf8");
}

function main() {
  const items = buildIndexItems();
  writeIndexFile(items);
  console.log("Generated selective-search-index.js with " + items.length + " entries.");
}

main();
